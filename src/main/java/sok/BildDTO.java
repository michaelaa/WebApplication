public class BildDTO {
    
    private String bildID;
    private String kategori;
    private String bildfil;

    /**
     * @return the bildID
     */
    public String getBildID() {
        return bildID;
    }

    /**
     * @param bildID the bildID to set
     */
    public void setBildID(String bildID) {
        this.bildID = bildID;
    }

    /**
     * @return the kategori
     */
    public String getKategori() {
        return kategori;
    }

    /**
     * @param kategori the kategori to set
     */
    public void setKategori(String kategori) {
        this.kategori = kategori;
    }

    /**
     * @return the bildfil
     */
    public String getBildfil() {
        return bildfil;
    }

    /**
     * @param bildfil the bildfil to set
     */
    public void setBildfil(String bildfil) {
        this.bildfil = bildfil;
    }
    

    
}
